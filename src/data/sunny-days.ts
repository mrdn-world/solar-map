// Average annual sunny/clear days by city/location
// Sources: WMO, Weather Atlas, CurrentResults, Weatherbase, Wikipedia
// "Sunny days" ≈ days with predominantly clear skies

export interface LocationData {
  id: number
  city: string
  cityRu: string
  country: string
  countryRu: string
  lat: number
  lon: number
  sunnyDays: number
  category: Category
}

export type Category = 'scorching' | 'sunny' | 'moderate' | 'cloudy' | 'gloomy'

function cat(days: number): Category {
  if (days >= 300) return 'scorching'
  if (days >= 250) return 'sunny'
  if (days >= 200) return 'moderate'
  if (days >= 150) return 'cloudy'
  return 'gloomy'
}

let _id = 0
function loc(city: string, cityRu: string, country: string, countryRu: string, lat: number, lon: number, sunnyDays: number): LocationData {
  return { id: _id++, city, cityRu, country, countryRu, lat, lon, sunnyDays, category: cat(sunnyDays) }
}

